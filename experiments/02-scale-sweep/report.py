"""Aggregate raw/<model>.jsonl sweep outputs into results.md.

Run after one or more `sweep.py --model <key>` runs:
    python report.py
"""

from __future__ import annotations

import glob
import json
import os
import statistics

HERE = os.path.dirname(os.path.abspath(__file__))


def load(path: str) -> tuple[dict, list[dict]]:
    meta, recs = None, []
    with open(path) as f:
        for line in f:
            if not line.strip():
                continue
            d = json.loads(line)
            if d.get("meta"):
                meta = meta or d
            else:
                recs.append(d)
    return meta, recs


def median(xs: list[int]) -> float:
    return statistics.median(xs) if xs else float("nan")


def pct(xs: list[int], cap: int) -> str:
    if not xs:
        return "-"
    return f"{100 * sum(1 for x in xs if x <= cap) / len(xs):.0f}%"


def main() -> None:
    runs = []
    for path in sorted(glob.glob(os.path.join(HERE, "raw", "*.jsonl"))):
        meta, recs = load(path)
        if meta and recs:
            runs.append((meta, recs))
    runs.sort(key=lambda mr: mr[0]["n_params"])

    out = []
    emit = out.append
    emit("# Experiment 02 — scale sweep results")
    emit("")
    emit("Generated by `report.py` from `raw/*.jsonl` (one file per model,")
    emit("produced by `sweep.py`). 93 multi-hop prompts from Anthropic's")
    emit("`lens-eval-multihop` set. All ranks are 1-based; rank 1 = the lens's")
    emit("top choice. See README.md for how to read this.")
    emit("")

    emit("## Headline: hidden-intermediate visibility vs model size")
    emit("")
    emit("`inter` = rank of the unstated intermediate concept (e.g. *Italy* "
         "in the boot→currency hop) at the **best** layer and position for "
         "it — a generous upper bound on workspace visibility. `control` = "
         "the identical search for an intermediate borrowed from a different "
         "prompt: the noise floor of the metric. The signal is the gap "
         "between the two.")
    emit("")
    emit("| model | params | layers | vocab | task: answer in model top-10 "
         "| inter median best rank | inter ≤10 | control median best rank "
         "| control ≤10 |")
    emit("|---|---|---|---|---|---|---|---|---|")
    for meta, recs in runs:
        inter = [r["inter_best"]["rank"] for r in recs]
        ctrl = [r["control_best"]["rank"] for r in recs if r.get("control_best")]
        ans_model = [r["answer_model_rank"] for r in recs]
        emit(
            f"| {meta['model_key']} | {meta['n_params']/1e6:.0f}M "
            f"| {meta['n_layers']} | {meta['vocab']} "
            f"| {pct(ans_model, 10)} "
            f"| {median(inter):.0f} | {pct(inter, 10)} "
            f"| {median(ctrl):.0f} | {pct(ctrl, 10)} |"
        )
    emit("")

    emit("## Answer preview: does the lens see the answer coming?")
    emit("")
    emit("Rank of the expected answer at the last prompt token: in the "
         "model's real output distribution vs the lens readout at its best "
         "layer (experiment 01's positive result, now across scale).")
    emit("")
    emit("| model | answer median rank (model) | answer median best rank "
         "(lens) | lens answer ≤10 |")
    emit("|---|---|---|---|")
    for meta, recs in runs:
        ans_model = [r["answer_model_rank"] for r in recs]
        ans_lens = [r["answer_lens_best"]["rank"] for r in recs]
        emit(
            f"| {meta['model_key']} | {median(ans_model):.0f} "
            f"| {median(ans_lens):.0f} | {pct(ans_lens, 10)} |"
        )
    emit("")

    emit("## Does solving the task come with a visible intermediate?")
    emit("")
    emit("Same intermediate metric, split by whether the model actually "
         "solves the prompt (answer in its top-10). If multi-hop answers "
         "were produced *via* a lens-visible intermediate, solved prompts "
         "should show much better ranks.")
    emit("")
    emit("| model | solved: n, median inter rank, ≤10 "
         "| unsolved: n, median inter rank, ≤10 |")
    emit("|---|---|---|")
    for meta, recs in runs:
        solved = [r for r in recs if r["answer_model_rank"] <= 10]
        unsolved = [r for r in recs if r["answer_model_rank"] > 10]
        s = [r["inter_best"]["rank"] for r in solved]
        u = [r["inter_best"]["rank"] for r in unsolved]
        emit(
            f"| {meta['model_key']} "
            f"| n={len(s)}, {median(s):.0f}, {pct(s, 10)} "
            f"| n={len(u)}, {median(u):.0f}, {pct(u, 10)} |"
        )
    emit("")

    emit("## Per-model detail: best and worst prompts for the intermediate")
    emit("")
    for meta, recs in runs:
        by_rank = sorted(recs, key=lambda r: r["inter_best"]["rank"])
        emit(f"### {meta['model_key']} ({meta['hf_id']})")
        emit("")
        emit("| prompt | intermediate | best rank | at layer | at token "
             "| answer rank (model) |")
        emit("|---|---|---|---|---|---|")
        for r in by_rank[:8] + by_rank[-3:]:
            ib = r["inter_best"]
            emit(
                f"| {r['name']} | {ib['matched']} | {ib['rank']} "
                f"| L{ib['layer']} | `{ib['pos_token']}` "
                f"| {r['answer_model_rank']} |"
            )
        emit("")

    path = os.path.join(HERE, "results.md")
    with open(path, "w") as f:
        f.write("\n".join(out) + "\n")
    print(f"wrote {path} ({len(runs)} models)")


if __name__ == "__main__":
    main()
